package operators;

public class OperatorMain4 {
    public static void main(String[] args) {

        int a = 1;
        int b = 10;


        System.out.println("Результат : " + (a == b));
        System.out.println("Результат : " + (a != b));
        System.out.println("Результат : " + (a < b));
        System.out.println("Результат : " + (a > b));
        System.out.println("Результат : " + (a >= b));
        System.out.println("Результат : " + (a <= b));



    }
}
